CREATE TABLE student (
   id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
   name VARCHAR(64) NOT NULL,
   surname VARCHAR(64) NOT NULL
);

CREATE TABLE teacher (
   id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
   name VARCHAR(64),
   surname VARCHAR(64),
   username VARCHAR(64),
   password VARCHAR(60)
);

CREATE TABLE evaluation (
   id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
   stream ENUM('FE', 'BE', 'UX', 'QA') NOT NULL,
   student_id BIGINT REFERENCES student(id),
   teacher_id BIGINT REFERENCES teacher(id),
   teacher_comment VARCHAR(255) NOT NULL,
   communication_grade ENUM('GOOD', 'AVERAGE', 'WEAK') NOT NULL,
   communication_comments VARCHAR(255),
   is_extramile BOOLEAN NOT NULL,
   is_extramile_comments VARCHAR(255),
   is_motivated BOOLEAN NOT NULL,
   motivation_comments VARCHAR(255),
   direction_comments VARCHAR(255) NOT NULL,
   ability_to_learn_grade ENUM('GOOD', 'AVERAGE', 'WEAK') NOT NULL,
   ability_to_learn_comments VARCHAR(255),
   overall_evaluation ENUM(
      'NOT_RECOMMENDED',
      'AVERAGE',
      'GOOD',
      'VERY_GOOD'
   ) NOT NULL,
   overall_comments VARCHAR(255) NOT NULL,
   created_at TIMESTAMP NOT NULL,
   updated_at TIMESTAMP NOT NULL
);


